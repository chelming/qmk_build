void eeconfig_init_keymap(void) {
    set_unicode_input_mode(UC_WINC);
    breathing_disable();
    rgblight_mode(RGBLIGHT_MODE_STATIC_LIGHT);
    rgblight_sethsv(58, 255, 80);
    backlight_enable();
    backlight_level(BACKLIGHT_LEVELS);
}

void matrix_scan_keymap(void) {
    led_t led_state = host_keyboard_led_state();
    if (!led_state.num_lock) {
        tap_code(KC_NUMLOCK);
    }
}

bool process_record_keymap(uint16_t keycode, keyrecord_t* record) {
    switch (keycode) {
        case LOWER:
            if (record->event.pressed) {
                layer_on(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
            } else {
                layer_off(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
            }
            return false;
        case RAISE:
            if (record->event.pressed) {
                layer_on(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
            } else {
                layer_off(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
            }
            return false;
        case ADJUST:
            if (record->event.pressed) {
                layer_on(_ADJUST);
            } else {
                layer_off(_ADJUST);
            }
            return false;
        case B_IRONY:
            if (record->event.pressed) {
                uint8_t mods = get_mods();
                send_unicode_string((mods & MOD_MASK_SHIFT) ? "⸮" : "‽");
            }
            return false;
        case SNAKE:
            if (record->event.pressed) {
                send_unicode_string("🐍");
            }
            return false;
        case POOP:
            if (record->event.pressed) {
                send_unicode_string("💩");
            }
            return false;
    }
    return true;
}
